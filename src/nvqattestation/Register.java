    private void txtRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtRegisterActionPerformed
        String insertUser = "INSERT INTO `user`(`name`, `username`, `password`) VALUES (?,?,?)";
        
        String name = txtRegUserName.getText();
        String username = txtRegUserName.getText();
        String passowrd = txtRegPassword.getText();
        
        User user = new User(name, username, passowrd);
        if(validation()){
            try {
                pst = database.con.prepareStatement(insertUser);
                pst.setString(1,user.getName());
                pst.setString(2,user.getUsername());
                pst.setString(3,user.getPassword());
                
                pst.executeUpdate();
                
                JOptionPane.showMessageDialog(null, "User Registration Succefully !");
                
                clearFields();
                
                this.dispose();
                UserLogin frame = new UserLogin();
                frame.setVisible(true);
            } catch (SQLException ex) {
                Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_txtRegisterActionPerformed

