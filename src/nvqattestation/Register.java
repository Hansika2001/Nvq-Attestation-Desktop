    boolean validation(){
        
            if(txtRegName.getText().isEmpty() || txtRegUserName.getText().isEmpty() || txtRegPassword.getText().isEmpty() || txtRegRePassword.getText().isEmpty()){
                txtRegName.requestFocus();
                JOptionPane.showMessageDialog(null, "All Fields Are Required");
                return false;
            }
            
            if(txtRegPassword.getText().length() < 6){
                txtRegPassword.requestFocus();
                JOptionPane.showMessageDialog(null, "Password Will be minimum 6 characters !","DELETE",JOptionPane.WARNING_MESSAGE);
                return false;
            }
            
//            String password = new String(txtRegPassword.getPassword());
//            String repassword = new String(txtRegPassword.getPassword());
//            if(password !=repassword){
//                txtRegPassword.setText("");
//                txtRegRePassword.setText("");
//                txtRegPassword.requestFocus();
//                JOptionPane.showMessageDialog(null, "Password and Re-Password not matched !","DELETE",JOptionPane.WARNING_MESSAGE);
//                return false;
//            }
            
            if(txtRegUserName.getText().length() < 2){
                txtRegUserName.requestFocus();
                JOptionPane.showMessageDialog(null, "Invalid UserName Please use minimum 2 or more characters !","DELETE",JOptionPane.WARNING_MESSAGE);
                return false;
            }
            
        try {
            String checkUsername="SELECT  `username` FROM `user` WHERE `username`=?";
            pst = database.con.prepareStatement(checkUsername);
            pst.setString(1,txtRegUserName.getText());
            rs = pst.executeQuery();
            
            if(rs.next()){
                JOptionPane.showMessageDialog(null,"This UserName is Already used !");
                txtRegUserName.requestFocus();
                return false;
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
        }
        return true;
    }

